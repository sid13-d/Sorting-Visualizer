async function mergeSort(elements,l, r){
    // console.log(l);
    if(l>=r){
        return;//returns recursively
    }
    var m =l+ Math.floor((r-l)/2);

    await mergeSort(elements,l,m);
   await mergeSort(elements,m+1,r);
   await merge(elements,l,m,r);
}

